def _misc_gallery_fetch_art_enabled() -> bool:
    raw = os.environ.get("MYPI_MISC_GALLERY_ART", "1").strip().lower()
    return raw not in ("0", "false", "no", "off")


def _misc_gallery_max_wrap_lines() -> int:
    raw = os.environ.get("MYPI_MISC_GALLERY_MAX_WRAP_LINES", "12").strip()
    try:
        n = int(raw)
    except ValueError:
        return 12
    return max(4, min(24, n))


